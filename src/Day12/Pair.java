package Day12;

public class Pair {

    private String entry;
    private String exit;

    public Pair(String a, String b){
        entry = a;
        exit = b;
    }

    public String getEntry() {
        return entry;
    }

    public void setEntry(String entry) {
        this.entry = entry;
    }

    public String getExit() {
        return exit;
    }

    public void setExit(String exit) {
        this.exit = exit;
    }



}
